class User {
  final String image;
  final String name;
  final String email;
  final String username;
  final String phone;
  final String dob;
  final bool isDarkMode;

  const User({
    required this.image,
    required this.name,
    required this.email,
    required this.username,
    required this.phone,
    required this.dob,
    required this.isDarkMode,
  });
}
